def calculate_age_in_days(age):
    """
    This function calculates the age in days
    :param age:
    :return: the age in days
    """
    return age * 365


def calculate_age_in_hours(age):
    """
    This function calculates the age in hours
    :param age:
    :return: the age in hours
    """
    return age * 365 * 24


def calculate_age_in_minutes(age):
    """
    This function calculates the age in minutes
    :param age:
    :return: the age in minutes
    """
    return age * 365 * 24 * 60
